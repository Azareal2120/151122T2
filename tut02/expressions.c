#include <stdio.h>

int main(void) {
    int expression1 = 7 / 2;
    printf("%d\n", expression1);
    
    double expression2 = (3.0 / 2) + 1;
    printf("%.2lf\n", expression2);
    
    char expression3 = 'a' + 5;
    printf("%c\n", expression3);
    
    char expression4 = 'F' - 'A' + 'a';
    printf("%c\n", expression4);
    
    return 0;
}
